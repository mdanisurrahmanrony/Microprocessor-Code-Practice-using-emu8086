.MODEL SMALL
.STACK 100H
.DATA 
MSG DB "Addition of 2 Numbers$" 

PROMPT1 DB 10,10,13,"ENTER A FIRST NUMBER: $"
PROMPT2 DB 10,13,"ENTER A SECOND NUMBER: $" 
PROMPT3 DB 10,13,"RESULT: $"
NUM1 DB ?,'$'
NUM2 DB ?,'$'

.CODE 

MAIN PROC
    
MOV AX,@DATA
MOV DS,AX


MOV AH,9
MOV DX,OFFSET MSG
INT 21H

MOV AH,9
MOV DX,OFFSET PROMPT1
INT 21H

MOV AH,1  
INT 21H
SUB AL,30H  
MOV NUM1,AL

MOV AH,9
MOV DX,OFFSET PROMPT2
INT 21H

MOV AH,1
INT 21H
SUB AL,30H
MOV NUM2,AL

MOV AH,9
MOV DX,OFFSET PROMPT3
INT 21H

MOV AL,NUM1
ADD AL,NUM2   
SUB AL,30H 


MOV AH,2
MOV DL,ANS
INT 21H  


MOV AH,4CH
INT 21H

ENDP

END MAIN